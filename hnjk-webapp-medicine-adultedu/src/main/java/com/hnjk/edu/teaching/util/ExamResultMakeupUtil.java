	/**
	 * 获取补考批次名称
	 * @param year
	 * @param month
	 * @return
	 */
	public static String getBatchName(String year, int month) {
		if(month>=2 && month <=8){
			year += AduConstants.BATCHNAME_SPRING;
		} else{
			year += AduConstants.BATCHNAME_SUMMER;
		}
		
		return year;
	}

