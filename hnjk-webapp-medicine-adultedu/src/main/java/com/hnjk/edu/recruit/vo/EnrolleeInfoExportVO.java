package com.hnjk.edu.recruit.vo;

import lombok.ToString;

import java.io.InputStream;


/*
 * 用于导出录取库的学生信息
 */
@ToString
public class EnrolleeInfoExportVO {
	private String no ;//序号,用于导出时空出一列
	private String enrolleeNo; //学号
	private String enrolleeName; //姓名
	private String enrolleeBrSchool; //学院
	private String enrolleeMajor; //专业
	private String enrolleeClass; //班级
	private String certNum;  //身份证验证码
	private InputStream barcode;//条形码
	private String eduOrderNo;// 教育订单号
	private Double money;// 金额
	private String memo;// 备注
	
	
	public Double getMoney() {
		return money;
	}
	public void setMoney(Double money) {
		this.money = money;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	public String getEduOrderNo() {
		return eduOrderNo;
	}
	public void setEduOrderNo(String eduOrderNo) {
		this.eduOrderNo = eduOrderNo;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public InputStream getBarcode() {
		return barcode;
	}
	public void setBarcode(InputStream barcode) {
		this.barcode = barcode;
	}
	public String getEnrolleeNo() {
		return enrolleeNo;
	}
	public void setEnrolleeNo(String enrolleeNo) {
		this.enrolleeNo = enrolleeNo;
	}
	public String getEnrolleeName() {
		return enrolleeName;
	}
	public void setEnrolleeName(String enrolleeName) {
		this.enrolleeName = enrolleeName;
	}
	public String getEnrolleeBrSchool() {
		return enrolleeBrSchool;
	}
	public void setEnrolleeBrSchool(String enrolleeBrSchool) {
		this.enrolleeBrSchool = enrolleeBrSchool;
	}
	public String getEnrolleeMajor() {
		return enrolleeMajor;
	}
	public void setEnrolleeMajor(String enrolleeMajor) {
		this.enrolleeMajor = enrolleeMajor;
	}
	public String getEnrolleeClass() {
		return enrolleeClass;
	}
	public void setEnrolleeClass(String enrolleeClass) {
		this.enrolleeClass = enrolleeClass;
	}
	public String getCertNum() {
		return certNum;
	}
	public void setCertNum(String certNum) {
		this.certNum = certNum;
	}	
}
