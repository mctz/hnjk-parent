package com.hnjk.edu.recruit.vo;

import java.io.Serializable;

public class StatisTrendRecruit implements Serializable {

	private static final long serialVersionUID = 168330896727353020L;

	private String recruitName;

	private Double apply;

	private Double confirm;

	private Double exam;

	private Double exemption;

	private Double enroll;

	private Double register;

	public StatisTrendRecruit() {
	}

	public StatisTrendRecruit(String recruitName, Double apply, Double confirm,
			Double exam, Double exemption, Double enroll, Double register) {
		this.recruitName = recruitName;
		this.apply = apply;
		this.confirm = confirm;
		this.exam = exam;
		this.exemption = exemption;
		this.enroll = enroll;
		this.register = register;
	}
	
	public StatisTrendRecruit(String recruitName, Double apply, Double confirm ) {
		this.recruitName = recruitName;
		this.apply = apply;
		this.confirm = confirm;
	}

	public String getRecruitName() {
		return recruitName;
	}

	public void setRecruitName(String recruitName) {
		this.recruitName = recruitName;
	}

	public Double getApply() {
		return apply;
	}

	public void setApply(Double apply) {
		this.apply = apply;
	}

	public Double getConfirm() {
		return confirm;
	}

	public void setConfirm(Double confirm) {
		this.confirm = confirm;
	}

	public Double getExam() {
		return exam;
	}

	public void setExam(Double exam) {
		this.exam = exam;
	}

	public Double getExemption() {
		return exemption;
	}

	public void setExemption(Double exemption) {
		this.exemption = exemption;
	}

	public Double getEnroll() {
		return enroll;
	}

	public void setEnroll(Double enroll) {
		this.enroll = enroll;
	}

	public Double getRegister() {
		return register;
	}

	public void setRegister(Double register) {
		this.register = register;
	}

}
