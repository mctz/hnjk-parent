package com.hnjk.edu.finance.vo;

import lombok.AccessLevel;
import lombok.Getter;
import lombok.Setter;
import lombok.ToString;

import java.io.Serializable;

/**
 * 缴费学生基本信息
 * 
 * @author Zik, 广东学苑教育发展有限公司
 * @since Nov 17, 2016 6:44:12 PM 
 * 
 */
@ToString
@Getter(value = AccessLevel.PUBLIC)
@Setter(value = AccessLevel.PUBLIC)
public class PayStudentInfoVO {

	private String studentName;
	private String studentNo;
	private String examCertificateNo;
	
	public PayStudentInfoVO(){}
	
	public PayStudentInfoVO(String studentName, String studentNo,
			String examCertificateNo) {
		super();
		this.studentName = studentName;
		this.studentNo = studentNo;
		this.examCertificateNo = examCertificateNo;
	}
	
}
