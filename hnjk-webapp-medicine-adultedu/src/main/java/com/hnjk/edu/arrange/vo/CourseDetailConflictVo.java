package com.hnjk.edu.arrange.vo;

public class CourseDetailConflictVo {
	private String teachcourseid;//教学班
	private String detailid;//排课详情id
	private String conflict;//冲突源id
	private String name;//冲突源名称
	private String type;//冲突类型（班级：class，老师：teacher，课室：room）
	private String days;//星期
	private String weeks;//周数
	private String timeperiodids;//上课时间
	public String getTeachcourseid() {
		return teachcourseid;
	}
	public void setTeachcourseid(String teachcourseid) {
		this.teachcourseid = teachcourseid;
	}
	public String getDetailid() {
		return detailid;
	}
	public void setDetailid(String detailid) {
		this.detailid = detailid;
	}
	public String getConflict() {
		return conflict;
	}
	public void setConflict(String conflict) {
		this.conflict = conflict;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDays() {
		return days;
	}
	public void setDays(String days) {
		this.days = days;
	}
	public String getWeeks() {
		return weeks;
	}
	public void setWeeks(String weeks) {
		this.weeks = weeks;
	}
	public String getTimeperiodids() {
		return timeperiodids;
	}
	public void setTimeperiodids(String timeperiodids) {
		this.timeperiodids = timeperiodids;
	}
}
