package com.hnjk.platform.system.model;

import javax.persistence.Transient;

import com.hnjk.core.support.base.model.BaseModel;

/**
 * 基础的日志model.
 * @author hzg
 *
 */
public class BaseLogHistoryModel extends BaseModel {
	
	@Transient
	private boolean enableLogHistory = false;//设置是否运行写日志

	public boolean isEnableLogHistory() {
		return enableLogHistory;
	}

	public void setEnableLogHistory(boolean enableLogHistory) {
		this.enableLogHistory = enableLogHistory;
	}

	
	
}
