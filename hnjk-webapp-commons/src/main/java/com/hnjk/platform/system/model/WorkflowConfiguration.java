package com.hnjk.platform.system.model;

import java.util.Date;

/**
 * 工作流配置.
 * <code>WorkflowConfiguration</code><p>
 * 
 * @author：广东学苑教育发展有限公司.
 * @since： 2011-3-23 下午03:00:52
 * @see 
 * @version 1.0
 */
public class WorkflowConfiguration implements java.io.Serializable{

	private static final long serialVersionUID = -3758153920826697023L;

	private String name;//流程
	
	private String cnname;
	
	private String type;
	
	private String location;
	
	private Integer version;
	
	private Date lastmodifyTime;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the cnname
	 */
	public String getCnname() {
		return cnname;
	}

	/**
	 * @param cnname the cnname to set
	 */
	public void setCnname(String cnname) {
		this.cnname = cnname;
	}

	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}

	/**
	 * @return the location
	 */
	public String getLocation() {
		return location;
	}

	/**
	 * @param location the location to set
	 */
	public void setLocation(String location) {
		this.location = location;
	}

	/**
	 * @return the version
	 */
	public Integer getVersion() {
		return version;
	}

	/**
	 * @param version the version to set
	 */
	public void setVersion(Integer version) {
		this.version = version;
	}

	/**
	 * @return the lastmodifyTime
	 */
	public Date getLastmodifyTime() {
		return lastmodifyTime;
	}

	/**
	 * @param lastmodifyTime the lastmodifyTime to set
	 */
	public void setLastmodifyTime(Date lastmodifyTime) {
		this.lastmodifyTime = lastmodifyTime;
	}
	
	
}
