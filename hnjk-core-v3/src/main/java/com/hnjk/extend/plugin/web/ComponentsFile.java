package com.hnjk.extend.plugin.web;

/**
 * 加载插件的实体类.<p>
 * 插件需要通过配置文件注册.配置文件通常放在plugin/plugin-config.xml中.<p>
 * @author： 广东学苑教育发展有限公司.
 * @since： 2009-4-30下午05:35:23
 * @see 
 * @version 1.0
 */
public class ComponentsFile {
	
	public static String fileType_Js="js";
	
	public static String fileType_Css="css";
	
	private String fileType;
	
	private String filePath;
	
	private String fileId;
	
	private String componentCode;

	public String getComponentCode() {
		return componentCode;
	}

	public void setComponentCode(String componentCode) {
		this.componentCode = componentCode;
	}

	public String getFilePath() {
		return filePath;
	}

	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}

	public String getFileType() {
		return fileType;
	}

	public void setFileType(String fileType) {
		this.fileType = fileType;
	}

	public String getFileId() {
		return fileId;
	}

	public void setFileId(String fileId) {
		this.fileId = fileId;
	}

	

	
}
