package com.hnjk.security.model;
/**
 * 用户适配器模型，提供给工作流使用
 * @author hzg
 *
 */
public class UserAdaptor implements java.io.Serializable{

	private static final long serialVersionUID = -7220634630112261670L;

	private String id;//用户ID
	private String name;//用户名
	private String email;//用户邮件
	private String phone;//用户电话
	private String[] deptIdArr;//用户所属组织集合
	private String[] roleIdArr;//用户所属角色集合
	
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String[] getDeptIdArr() {
		return deptIdArr;
	}
	public void setDeptIdArr(String[] deptIdArr) {
		this.deptIdArr = deptIdArr;
	}
	public String[] getRoleIdArr() {
		return roleIdArr;
	}
	public void setRoleIdArr(String[] roleIdArr) {
		this.roleIdArr = roleIdArr;
	}
}
