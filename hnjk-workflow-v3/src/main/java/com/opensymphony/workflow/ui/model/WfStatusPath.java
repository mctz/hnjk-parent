package com.opensymphony.workflow.ui.model;

import java.util.Map;

/**
 * 流程图模型-path
 * <code>WfStatusPath</code><p>
 * <pre>
 *  paths:{
		  	path10:{from:'rect1',to:'rect2', dots:[],text:{text:'TO 任务1'},textPos:{x:-6,y:-6}},
		   	path11:{from:'rect2',to:'rect3', dots:[],text:{text:'TO 分支'},textPos:{x:-10,y:-7}},
		   	path12:{from:'rect3',to:'rect5', dots:[],text:{text:'TO 任务3'},textPos:{x:-1,y:-8}},
		   	path15:{from:'rect9',to:'rect8', dots:[],text:{text:'TO 结束'},textPos:{x:-2,y:-6}},
		   	path20:{from:'rect5',to:'rect9', dots:[],text:{text:'TO 任务5'},textPos:{x:0,y:-10}}
		   }
	 </pre>
 * @author：广东学苑教育发展有限公司.
 * @since： 2011-3-30 上午10:34:05
 * @see 
 * @version 1.0
 */
public class WfStatusPath implements java.io.Serializable{
	
	private static final long serialVersionUID = -7040707432885347064L;

	private String from;
	
	private String to;
	
	private String[] dots;
	
	private Map<String, Object> text;
	
	private Map<String, Object> textPos;

	/**
	 * @return the from
	 */
	public String getFrom() {
		return from;
	}

	/**
	 * @param from the from to set
	 */
	public void setFrom(String from) {
		this.from = from;
	}

	/**
	 * @return the to
	 */
	public String getTo() {
		return to;
	}

	/**
	 * @param to the to to set
	 */
	public void setTo(String to) {
		this.to = to;
	}

	/**
	 * @return the dots
	 */
	public String[] getDots() {
		return dots;
	}

	/**
	 * @param dots the dots to set
	 */
	public void setDots(String[] dots) {
		this.dots = dots;
	}

	/**
	 * @return the text
	 */
	public Map<String, Object> getText() {
		return text;
	}

	/**
	 * @param text the text to set
	 */
	public void setText(Map<String, Object> text) {
		this.text = text;
	}

	/**
	 * @return the textPos
	 */
	public Map<String, Object> getTextPos() {
		return textPos;
	}

	/**
	 * @param textPos the textPos to set
	 */
	public void setTextPos(Map<String, Object> textPos) {
		this.textPos = textPos;
	}
	
	
}
